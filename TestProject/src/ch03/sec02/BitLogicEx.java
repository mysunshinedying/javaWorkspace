package ch03.sec02;

public class BitLogicEx {
    public static void main(String[] args) {
        //정수를 이진수 bit으로 구성하고 동일 위치 bit끼리의 논리 연산
        System.out.println("45 & 25 = " + (45 & 25));
        System.out.println("45 | 25 = " + (45 | 25));
        System.out.println("45 ^ 25 = " + (45 ^ 25));
        System.out.println("~45 = " + (~45));


    }
}
